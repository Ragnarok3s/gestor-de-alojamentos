const fs = require("fs");
const file = "server.js";
let text = fs.readFileSync(file, "utf8");
text = text.replace(/Quantos meses \(1\uFFFD12\)/g, "Quantos meses (1–12)");
text = text.replace(/Base \uFFFD\/noite/g, "Base €/noite");
text = text.replace(/Pre\u00E7o base \uFFFD\/noite/g, "Preço base €/noite");
text = text.replace(/Pre\u00E7o \uFFFD\/noite/g, "Preço €/noite");
text = text.replace(/Caracter\uFFFDsticas/g, "Características");
text = text.replace(/s\uFFFD o/g, "só o");
text = text.replace(/\uFFFDcone/g, "ícone");
text = text.replace(/\uFFFDcones/g, "Ícones");
text = text.replace(/ \uFFFD /g, " · ");
text = text.replace(/\${b\.property_name} \uFFFD \${b\.unit_name}/g, "${b.property_name} – ${b.unit_name}");
text = text.replace(/\${b\.guest_phone\|\|'-'} \uFFFD \${b\.guest_email}/g, "${b.guest_phone||'-'} · ${b.guest_email}");
text = text.replace(/\${dayjs\(b\.checkin\)\.format\('DD\/MM'\)} \uFFFD \${dayjs\(b\.checkout\)\.format\('DD\/MM'\)}/g, "${dayjs(b.checkin).format('DD/MM')} &rarr; ${dayjs(b.checkout).format('DD/MM')}");
text = text.replace(/\${u\.property_name} \uFFFD \${u\.name}/g, "${u.property_name} – ${u.name}");
text = text.replace(/\${b\.property_name} \uFFFD \${b\.unit_name}/g, "${b.property_name} – ${b.unit_name}");
text = text.replace(/\uFFFD \${eur\(b\.total_cents\)}/g, "€ ${eur(b.total_cents)}");
fs.writeFileSync(file, text, "utf8");
